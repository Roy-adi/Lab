import java.util.Scanner;

class multiply{
      public static void main(String[]args){
  
       Scanner SC = new Scanner(System.in);  // To input from user 
       System.out.println(" input a no ");
        int a = SC.nextInt();               // input in form of int

       System.out.println(" input another no");
       int b = SC.nextInt();
       
      int multi = a * b;                  // multiply those no & store in a int
       System.out.println(multi);         // print the product
}

}
       
       
       
