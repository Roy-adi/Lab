
class float1{
    public static void main(String[] args)
    {
  
        // f is to ensures that numbers are float DATA TYPE
        float f1 = 2.5f;
        float f2 = 4.6f;
  
        // to store the multiplied value
        float m = f1 * f2;
  
        // to print the product
        System.out.println(m);
    }
}